     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $reservations = Reservation::leftJoin('restaurants as r', 'res_restaurant_id', '=', 'r.id')
            ->leftJoin('halls as h', 'res_hall_id', '=', 'h.id')
            ->leftJoin('tables as t', 'table_id', '=', 't.id')
            ->select('reservations.*', 't.table_number', 'h.name as hall_name', 'r.name as res_name', 'r.address')
            ->get();
        return view('admin.pages.reservations', ['reservations' => $reservations ]);
    }

